package d18_12_Demo;

public class TypeCasting 
{
	public static void main(String [] args)
	{
		//typecasting
		//implicit
		byte a = 10;
		int b =a;
		System.out.println(a);
		System.out.println(b);
		
		int c = 20;
		double d = c;
		System.out.println(c);
		System.out.println(d);
	}
	

}
